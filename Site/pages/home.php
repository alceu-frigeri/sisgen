        <h2>Bem vindo ao SBAI 2017 </h2>
        <hr>
        <h4><B>Apresentaçăo</B></h4>
        <p align="justify">
	    O Simpósio Brasileiro de Automaçăo Inteligente (SBAI) é o principal evento científico em automaçăo inteligente na América Latina, sendo patrocinado pela Sociedade Brasileira de Automática (SBA). Sua décima terceira ediçăo ocorrerá em Porto Alegre - RS, no período de 01 a 04 de outubro de 2017, sob organizaçăo da Universidade Federal do Rio Grande do Sul (UFRGS) e Pontifícia Universidade Católica do Rio Grande do Sul (PUCRS).
	</p>     <p align="justify">
	    Seguindo a tradiçăo das ediçőes anteriores, o XIII SBAI reunirá pesquisadores e profissionais que atuam nas áreas ancoradas em todos os aspectos de Sistemas e Automaçăo Inteligentes, como robótica, automaçăo, otimizaçăo, instrumentaçăo. Săo igualmente desejáveis trabalhos com contribuiçăo teórica de inteligęncia computacional ŕs áreas de Automaçăo e de Sistemas, assim como trabalhos com aplicaçőes e desenvolvimento que tratem de problemas de interesse do mundo real.
	</p>     <p align="justify">
	    O simpósio será realizado ao longo de 4 dias de sessőes técnicas, com palestras convidadas, workshops, minicursos e um evento de confraternizaçăo.
	</p>     <p align="justify">


            
