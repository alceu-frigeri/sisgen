</p><h3>Minicursos</h3><hr>
Pesquisadores de notório saber apresentarão aos participantes inscritos nesta atividade minicursos de até quatro horas de duração sobre temas de interesse. Estão confirmados os seguintes minicursos:
<ul>
<li>	Minicurso 01: Prof. Djalma Mosqueira Falcão - "As Redes Elétricas Inteligentes e o Futuro dos Sistemas de Energia Elétrica";
<ul>
    <li>Cenário atual. Definições e conceitos básicos de redes elétricas inteligentes. Tecnologias viabilizadoras: medição inteligente, tecnologias de informação e comunicação, sincrofasores, geração distribuída, etc. Aplicações em redes de distribuição: auto restabelecimento, controle de tensão, gerenciamento de perdas, etc. 
	Aplicações no sistema interligado: redes de PMUs, WAMPAC, etc. Situação no Brasil e no mundo. Visão de futuro.
</ul>
<li>	Minicurso 02: Profs. Eugênio de Bona Castelan Neto e Valter Júnior de Souza Leite - "Modelagem e controle fuzzy-TS de  sistemas não-lineares e com atraso nos estados";
<ul>
    <li>Neste mini-curso, busca-se evidenciar a importância da consideração da validade regional dos modelos fuzzy de tipo Takagi-Sugeno (T-S) para o desenvolvimento de ferramentas de análise e síntese de sistemas de controle não lineares,  e discutir problemas associados à complexidade desses modelos dos pontos de vista de implementação numérica dos  algoritmos e de implementação computacional dos controladores. Serão considerados sistemas não lineares discretos no tempo, inicialmente sem a presença de atrasos e, a seguir, sistemas com atrasos nos estados.

Modelos fuzzy T-S têm sido investigados no decorrer das últimas décadas principalmente por propiciarem o desenvolvimento de metodologias que possuem caráter sistemático para análise e projeto de sistemas de controle não lineares, além de propiciar soluções numéricas via técnicas baseadas em desigualdades matriciais lineares (LMIs, do inglês linear matrix inequalities). Embora diversos resultados sejam encontrados na literatura, existem questões de motivação prática que devem também ser consideradas no contexto do controle fuzzy baseado em modelo (FMB, do inglês Fuzzy Model Based). Primeiramente, as técnicas de modelagem fuzzy T-S  garantem a convexidade do modelo e/ou a sua precisão de representação somente para uma determinada região do espaço de estados. Portanto, para estratégias de controle que dependem da propriedades de convexidade, a estabilidade do sistema de malha fechada formado pelo sistema não linear realimentado pela lei de controle fuzzy deve ser estudada no contexto de estabilidade local. Assim, é fundamental a determinação de regiões de estabilidade para o sistema de malha fechada. Essa importante característica dos modelos fuzzy T-S raramente é considerada na literatura, podendo implicar em perda de desempenho e até mesmo instabilidade do sistema em malha fechada. Outro problema inerente à utilização de modelos fuzzy T-S diz respeito ao aumento exponencial de complexidade do modelo com o número de não linearidades presentes no sistema, principalmente quando se busca descrever de forma exata a dinâmica do sistema a controlar. Isso implica no aumento da complexidade numérica dos algoritmos para análise e projeto, assim como no aumento da complexidade de implementação de leis de controle.

</ul>
<li>	Minicurso 03: Profa. Silvia Silva da Costa Botelho e Paulo Lilles Jorge Drews Junior - "Aprendizado Profundo - O que eu tenho a ver com isso?!";
<ul>
    <li> Introdução aos métodos de aprendizado profundo com aplicações em reconhecimento de imagens, reconstrução de padrões, tradução de idiomas, processamento de áudio, controle, dentre outros.  Apresentação colaborativa de ferramentas e brainstorming com os participantes. 
	Propostas de uso em Automação e Robótica (imageamento, veículos autônomos, síntese de som e e imagem)
</ul>
<li>	Minicurso 04: Prof. Walter Fetter Lages - "Robot Operational System (ROS): Introdução e Implementação de Controladores";
<ul>
<li>Este curso apresenta uma introdução ao Robot Operating System (ROS) com
enfoque na implementação de controladores e sua interação com robôs reais ou
simulados no Gazebo.  Os conceitos básicos do ROS são abordados no contexto
de sua utilização na implementação de controladores avançados para robôs.

Apesar do ROS ser um framework largamente utilizado atualmente, a sua
documentação e os exemplos abordando controladores são pobres a quase todas
as implementações são baseadas em controladores single input, single output
(SISO) utilizando a lei de controle Proporcional, Integral, Derivativa
(PID).  A maioria das referências e livros sobre o ROS sequer cobre a
implementação de controladores.  Por outro lado, robôs são em geral,
sistemas não lineares multi input, multi output (MIMO), para os quais o uso
de controladores PID independentes para cada grou de liberdade (DoF) não é
adequado se é desejado alto desempenho.

Este curso aborda de forma genérica a implementação de controladores no ROS
e seu enfoque pode ser usado para a implementação de qualquer lei de
controle (linear, não linear, SISO ou MIMO) enquanto que os controladores
disponíveis nos pacotes padrão do ROS como o ros_controllers são apenas
representativos de controladores SISO.

O funcionamento interno do laço de tempo real do ROS é explicado em detalhe,
motivando a implementação de controladores avançados no ROS.

Do ponto de vista de Engenharia de Controle, o ROS apresenta algumas
particularidades com relação aos controladores.  Uma delas é relacionada à
nomenclatura, já que a denominação de controlador no ROS não necessariamente
refere-se à um controlador na nomenclatura de Sistemas de Controle.  Um
controlador no ROS é um plugin para o gerenciador de controladores que
implementa a interface Controller e tipicamente atua a junta através de
algum objeto que exponha uma JointCommandInterface e obtém dados de sensores
através de um objeto que exponha uma JointStateInterface.  Note que um
controlador ROS pode realizar uma função que não necessariamente é a função
de um controlador em us sistema de controle.  Um exemplo clássico é o
JointStateController, que por seu nome parece ser um controlador de juntas
no espaço de estados, mas que na realidade é apenas um publicador dos
valores das posições e velocidades das juntas.

O ROS não é um sistema de tempo real.  No entanto, ele possui alguma
capacidade de tempo real quando executado em um sistema Linux que suporte
tempo real através dos patches PREEMP_RT, RTAI ou XENOMAI e a efetiva
implementação de controladores em tempo real utilizando tais patches é
discutida.

Como os controladores no ROS são plugins carregados pelo gerenciador de
controladores, o seu uso para carta, descarga, ativação e desativação de
controladores é abordado juntamente com os principais pacotes do ROS para
implementação de controladores, mais especificamente: control_toolbox,
controller_interface, controller_manager, hardware_interface,
realtime_tools, effort_controllers, joint_state_controller,
ros_control_gazebo, ros_control_gazebo_plugin, robot_state_publisher, entre
outros.

São apresentados em detalhes a implementação de um controlador por torque
calculado para um manipulador robótico, um controlador linearizante e um
controlador não linear para um robô móvel, além do uso do controladores de
bypass e PID disponíveis nos pacotes ROS.

Palavras-chave:

ROS, Introdução, Controlador, Implementação Tempo real, Controlador não linear.

Syllabus:

Introdução ao ROS, Descrição de robôs no ROS, Unified Robot Description
Format (URDF), Visualização no rviz, Simulação no Gazebo, Mensagens,
serviços e aões, Controlador bypass, Controlador PID, Gerenciador de
controladores, Laço de tempo real do ROS, Implementação de controladores,
Suporte de tempo real, Interação com o simulador Gazebo, Interação com robô
real.

</ul>
