<h4><b>	<a target="_blank" href="http://lattes.cnpq.br/5416767068643649">Prof. Marco Henrique Terra</a></b></h4>
Universidade de Săo Paulo (USP) - Brasil
	
<p>

<p align="justify">
<p>

<p align="justify">

Possui graduaçăo em Engenharia Elétrica pela Universidade Estadual Paulista Júlio de Mesquita Filho (1986), mestrado em Engenharia Elétrica pela Universidade de Săo Paulo (1990), doutorado em Engenharia Elétrica pela Universidade de Săo Paulo (1995) e pós-doutorado pela Universidade da Califórnia em Los Angeles (2002). Coordenador do comitę de robótica e ex-presidente da Sociedade Brasileira de Automática (SBA). Editor associado das Revista Controle & Automaçăo da SBA e do Journal of Applied Mathematics. Tem revisado artigos para mais de trinta revistas científicas e para o Mathematical Reviews Database da Sociedade Americana de Matemática. Assessor ad hoc das agęncias brasileiras de apoio a pesquisa e do Research Grants Council (RGB) of Hong Kong. É professor titular do Departamento de Engenharia Elétrica da USP em Săo Carlos e Bolsista de Produtividade em Pesquisa do CNPq - Nível 1C. Foi coordenador do Núcleo de Apoio a Pesquisa da USP para a criaçăo de um Centro de Robótica em Săo Carlos, assessor da pró-reitoria de pesquisa da USP e Prefeito do Campus da USP em Săo Carlos. Palestrante convidado pelas Naçőes Unidas para falar sobre Robótica em um encontro bilateral entre Brasil e Uruguai para desenvolvimento de pesquisa. Coordenador do Instituto Nacional de Cięncia e Tecnologia (INCT) em Sistemas Autônomos Cooperativos Aplicados em Segurança Nacional e Meio Ambiente. Tem experięncia na área de Engenharia Elétrica, controle de sistemas, atuando principalmente nos seguintes temas: controle robusto, filtragem robusta e robótica.



