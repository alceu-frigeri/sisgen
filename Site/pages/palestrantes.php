<img class="img-responsive img-center logo" src="./images/Pal_Jose_Principe.jpg" alt="">
<h4><b>	<a target="_blank" href="http://cnel.ufl.edu/people/people.php?name=principe ">Prof. Jose C. Principe</a></b></h4>
University of Florida - EUA
<p>

<p align="justify">

Jose C. Príncipe possui o título de Engenheiro Eletricista (Universidade do Porto), Mestrado e Doutorado em Engenharia Elétrica pela Universidade da Flórida e é Doutor Honoris Causa pela Universita Mediterranea em Reggio Calabria, Italia. Dr. Príncipe é professor na Universidade da Florida desde 1987 e desde 2002 é Distinguished Professor of Electrical and Biomedical Engineering na mesma universidade. Em 1991 ele criou o Computational NeuroEngineering Laboratory com objetivo de fomentar a pesquisa em tópicos relacionados a processamento de sinais e informaçăo proveniente de sistemas biológicos. Por suas contribuiçőes, ele recebeu os pręmios Gabor Award da International Neural Network Society, Career Achievement Award da IEEE Engineering in Medicine and Biology Society e o IEEE Neural Network Pioneer Award da IEEE Computational Intelligence Society. Dr. Príncipe é Fellow das sociedades IEEE e AIMBE, ex-presidente da International Neural Network Society e ex-membro do Advisory Science Board da FDA e atual vice-presidente da IEEE Engineering Medicine and Biology Society. Possui atualmente mais de 700 publicaçőes em periódicos, 20 patentes concedidas, é coautor de 5 livros, além de ter orientado 81 teses de doutorado e 65 dissertaçőes de mestrado. Seus principais interesses săo processamento de sinais variantes no tempo através de modelos neurais adaptativos, teoria da informaçăo, reconhecimento de padrőes, interfaces cérebro-máquina e dinâmicas cerebrais.
</p>


<img class="img-responsive img-center logo" src="./images/Pal_FrankAllgoewer.jpg" alt="">
<h4><b>	<a target="_blank" href="http://www.ist.uni-stuttgart.de/institut/mitarbeiter/Allgoewer/?__locale=en">Prof. Frank Allgöwer</a></b></h4>
Universität Stuttgart - Alemanha
<p>
<p align="justify">
	
	

Frank Allgöwer estudou Engenharia Cibernética e Matemática Aplicada nas universidades de Stuttgart e da California, respectivamente. Atualmente é director do Institute for Systems Theory and Automatic Control e professor na Universidade de Stuttgart, Alemanha. Antes disso foi professor na universidade ETH Zurich e professor/pesquisador visitante no California Institute of Technology, na NASA Ames Research Center, na DuPont Company e na Universidade da California. Dr. Allgöwer é editor/editor associado dos periódicoa Automatica, Journal of Process Control, Journals of Robust and Nonlinear Control, IET Control Theory and Applications, entre outros. Faz parte dos conselhos da German Society for Measurement and Control (GMA), da IEEE Control System Society e do Comitę Técnico do IFAC para Sistemas Năo-lineares. Frank Allgöwer publicou mais de 150 artigos científicos e recebeu diversos pręmios como o IEEE distinguished lectureship e o Pręmio Leibniz, que é o pręmio científico mais importante concedido pelo Deutsche Forschungsgemeinschaft (DFG). Seus interesses de pesquisa săo a análise e controle de sistemas năo-lineares, com aplicaçőes em microscópios de força atômica, sistemas biológicos e biomédicos.

<img class="img-responsive img-center logo" src="./images/Pal_Shimon_Nof.jpg" alt="">
<h4><b>	<a target="_blank" href="https://engineering.purdue.edu/~nof/">Shimon Nof</a></b></h4>
Purdue University - EUA 

<p>

<p align="justify">

Shimon Nof é Engenheiro Industrial e Mestre pela universidade Technion, Israel e doutor em Engenharia Industrial pela Universidade de Michigan, Ann Arbor. Atualmente é professor de Engenharia Industrial e director do PRISM (Production, Robotics and Integration Software for Manufacturing & Management) Center da Purdue University. Também foi professor/pesquisador visitante no MIT e em universidades no Chile, EU, Hong Kong, Israel, Japăo, México, Filipinas e Taiwan. Dr. Nof recebeu em 2002 a Engellerger Medal for Robotics Education e é Fellow do Institute of International Education, Fellow e Presidente da International Foundation of Production Research e menbro do comitę do IFAC para sistemas de manufatura e logística. É coautor de mais de 450 artigo e autor/editor de 13 livros incluindo  as obras "Handbook of Industrial Robotics" e "International Encyclopedia of Robotics" ambos ganhadores do pręmio "Most Outstanding Book in Science and Engineering" dado pela Association of American Publishers. Seus principais interesses săo nas áreas de robótica colaborativa, produçăo integrada, sistemas ciber-físicos e tomada de decisăo inteligente.


<img class="img-responsive img-center logo" src="./images/Pal_Harald_Mathis.jpg" alt="">
<h4><b>	<a target="_blank" href="https://www.fit.fraunhofer.de/en/fb/life/biomos.html">Harald Mathis </a></b></h4>
Fraunhofer Institute for Applied Information Technology FIT - Alemanha
 
<p>

<p align="justify">

Harald Mathis é doutor pela Universidade Saarland, em Saarbrücken, Alemanha. Em 2000 assumiu o ensino de bioquímica na Universidade Friedrich-Schiller, Alemanha, em 2001 tornou-se chefe do Adaptive Biophotonics Group na Fraunhofer BioMIP Research Unit e desde 2004 é chefe grupo de pesquisa "Biomolecular Optical Systems - BioMOS" na mesma universidade. Desde 2010 trabalha no Fraunhofer Institute for Applied Information Technology  e leciona Informática Industrial e Engenharia de Sistema Biomédicos na Universidade Hamm-Lippstadt University, Alemanha. Em paralelo ŕ sua carreira científica, Dr. Mathis fundou várias empresas: a COMO GmbH (hoje Dyomics GmbH) em em 1998, a fluIT Biosystems GmbH em 2005 e a DiagnostX GmbH em 2007. Além disso é coordenador do grupo de trabalho "Microsystems for Biotechnology and Lifesciences", o qual discute atividades de pesquisa nos âmbitos nacional e Europeu. Seus interesses săo nas áreas de cięncia da computaçăo aplicada ŕ biologia, monitoramento de parâmetros biológicos e tecnologias assistivas.


<img class="img-responsive img-center logo" src="./images/Pal_Jose_Cury.gif" alt="">
<h4><b>	<a target="_blank" href="http://lattes.cnpq.br/0287055549224841">Prof. Jose Eduardo Ribeiro Cury </a></b></h4>
Universidade Federal de Santa Catarina (UFSC) - Brasil

<p>

<p align="justify">

Possui graduaçăo em Engenharia Elétrica pela Universidade de Brasília (1975), mestrado em Engenharia Elétrica pela Universidade Estadual de Campinas (1979) e Doctorat D'état Es Sciences - Universite de Toulouse III (Paul Sabatier) (1983). Atualmente é professor titular da Universidade Federal de Santa Catarina e Bolsista de Produtividade em Pesquisa do CNPq - Nível 1B. Foi editor associado do IEEE Transacions on Automatic Control no período de 2005 a 2008 e atualmente é editor associado do Journal Européen de Systčmes Automatisés (JESA). Foi professor visitante em diversas universidades europeias e pesquisador visitante na Carnegie Mellon University (CMU) em 1995-96, na Université de Nantes em 2007-08, e no Instituto Gulbenkian de Cięncia (IGC-Lisboa) em 2015-16. Tem experięncia na área de Engenharia Elétrica, com ęnfase em Teoria de Controle de Sistemas Dinâmicos, atuando principalmente nos seguintes temas: controle de sistemas a eventos discretos, e sistemas híbridos, aplicaçőes em sistemas de manufatura, sistemas de tráfego e redes biológicas

