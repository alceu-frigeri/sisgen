        <h2>Paper Submission</h2>
        <hr>    
        
        <p align="justify">
            All manuscripts as well as Invited/Special Session Proposals must be electronically submitted through the <a href="https://ifac.papercept.net/">PaperPlaza Conference Manuscript Management System</a>. Guidelines for the preparation of manuscripts are provided on the <a href="http://www.ifac-control.org/events/information-for-ifac-authors">IFAC</a> website. Authors are advised to read PaperCept's <a href="http://css.paperplaza.net/conferences/manuals/authorgetstarted.pdf">Getting Started Manual</a> for Authors. Only PDF files compliant with the IFAC Publications Requirements are acceptable for publication. The procedure to generate pdf files and compliance and diagnostic tools are provided in the support section of PaperCept.

        </p> 

        <p align="justify">
            All papers will be accepted with the understanding that the authors will present them at the Telematics symposium. At least one author of every accepted paper will be required to register for the symposium before uploading the final version. 
        </p> 
