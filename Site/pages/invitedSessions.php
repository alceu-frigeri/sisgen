            Dennis Brandão (USP-SC, BR)<br>
        </div>
        <div class="readmore">
            <p  class="description">
                <b><u>Description:</u></b> 
                Industry has always been and continues being a backbone in modern 
                societies. However, to be competitive and sustainable, it has to continue its quest 
                for system-wide high efficiency and high flexibility. This requires tight synchrony 
                between all industrial system departments from management and planning to 
                execution, maintenance, production, supply, etc. and adjusting to varying 
                demand and potentially variable resources. This tight synchrony, which is at the 
                core of recent developments such as Industry 4.0, pushes the limits of the 
                underlying communication infrastructure that needs to support increasingly 
                complex, heterogeneous and varying information requirements, from high speed 
                isochronous horizontal flows to massive vertical flows of process monitoring, 
                high-definition multimedia streams, complex configurations, etc. These 
                requirements impose major development challenges, particularly when non-
                functional properties must be enforced, such as real-time response, traffic 
                isolation and reconfigurability.
            </p>
            <p class="description">In this context, certain technologies gain particular relevance, such as 
                Industrial Ethernet protocols, particularly Real-Time Ethernet protocols, and 
                synchronization protocols such as the Precision Time Protocol. More recently, 
                standardization efforts such Time-Sensitive Networking promise to lay the 
                grounds to efficiently support traffic with highly heterogeneous requirements. 
                Another related framework is that of Software-Defined Networking which is 
                making its way from the telecommunications world into other domains such as 
                industrial automation.</p>
                <p class="description">Therefore, this session aims at bringing together researchers and practitioners
                    to presenting cutting-edge research in all aspects of industrial communications, 
                    from new concepts and protocols, to protocol modeling and analysis, system 
                    analysis and design, experience reports, etc.</p>
                    <p>Topics of particular interest include, but are not limited to, the following:
                        <ul>
                            <li>Industrial (Real-Time) Ethernet</li>
                            <li>Evolutions of fieldbuses</li>
                            <li>Clock synchronization</li>
                            <li>Open/flexible communications</li>
                            <li>Composability in communications</li>
                            <li>Dependable communications</li>
                            <li>On-line bandwidth management</li>
                            <li>Ultra-low communications jitter</li>
                            <li>Time-Sensitive Networking</li>
                            <li>Software-Defined Networking</li>
                        </ul> 
                    </p>
