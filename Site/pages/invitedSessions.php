            <span class="title">Networked control</span><br>
            <span class="session">(session code: <span class="session-code">ahgm9</span> )</span><br>
            João Manoel Gomes da Silva Jr (UFRGS, BR)<br>
            Sophie Tarbouriech (LAAS, FR)<br>
        </div> 
        <div class="readmore">
        <p  class="description">
            <b><u>Description:</u></b> 
            Control systems are nowadays implemented over communication
            networks, in the so-called networked control paradigm.
            Networked control systems (NCSs) are spatially distributed
            systems for which the communication between sensors,
            actuators, and controllers is supported by a shared
            communication network.In this case, communication delays,
            package losses and heavy temporary load of computation in a
            processor can lead to significant variations on the
            sampling time. These variations can dramatically affect the
            stability properties and the performance of
            the control system. In this case, classical techniques to
            model linear and time-invariant systems, like the use of
            the Z-transform and discrete-time models obtained from the
            assumption of constant sampling time are no longer
            applicable to assess the performance or even to design the
            control laws. Besides, systems to be controlled
            usually have a nonlinear behavior and under the network
            delays and aperiodic sampling their control become still
            more difficult, especially with a prescribed
            performance. It should also be pointed out that the vast
            majority of controlled systems involving
            communications have either been designed without the joint
            control-coding approach or by using it partially in an ad
            hoc way. Nevertheless, separating the design of the
            controllers and coders can be very suboptimal, incurring
            some severe performance losses and significant additional
            costs.
        </p>
        <p  class="description">
            The aim of this section is to  cover different topics
            related to networked control, including for instance issues
            related to:  communication delays,tele-operation, sampled
            data control, event trigger control, self-trigger control,
            quantization effects,  etc., as well as applications of
            networked control on robotics and embedded systems.
        </p>
         <p  class="description">
            This would be a good opportunity to put together
            people from control and network communities and potentially
            promote fruitful discussions considering different points
            of view of the same problem.
        </p>

