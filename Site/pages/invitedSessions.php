    </div>
    <div class="readmore">
        <p  class="description">
            <b><u>Description:</u></b> 
            The evolution of industrial networks can be summarized as a constant battle to define the 

            universal technology that integrates field devices and applications. Since the Fieldbus wars in 

            the 1980s, diverse wired solutions have been proposed. However, this scenario has been 

            changing due to the introduction of industrial wireless sensor networks. In the last 10 years, the 

            development of deterministic scheduling techniques and redundant routing algorithms, 

            advances in dependability and security embedded systems, and energy saving issues have 

            brought wireless sensor networks into the industrial domain. This new communication paradigm 

            is governed by a de facto standard, the IEEE 802.15.4, and more recently also by the IEEE 

            802.15-e. However, there are signs of a new battle on the horizon with the new publicly 

            available specifications IEC 62591 (WirelessHART), IEC 62734 (ISA100.11a), and IEC 62601 

            (WIA-PA). Therefore, there is a need to investigate and disseminate research directions coming 

            from adoption of wireless communication to automation domain. The challenge is exploiting 

            network-related effects such as jitter, end-to-end reliability, robustness and adaptation in order 

            to attain the stringent dependability requirement of automation applications.
        </p>
        <p class="description">
            The aim of this session is to provide insight on effectiveness and adoption of wireless 

            communication in automation. This session seeks contributions related to the adaption of 

            methods, tools and practices in industrial wireless communication for automation systems. 

            Therefore, this special session will focus on (but not be limited to) the following topics:

            <ul>
                <li>Deployment and case studies of wireless sensor and actuator systems in automation;</li>
                <li>Performance evaluation, simulation, RF measurements and modeling of industrial

                    wireless sensor networks;</li>
                    <li>Methodology, framework, and formal models for dependability evaluation in wireless

                        communication for automation applications;</li>
                        <li>Emerging applications for industrial wireless sensor networks;</li>
                        <li>Techniques for adaptive industrial wireless sensor network in automation environments;</li>
                        <li>Employment of different techniques for standards improvements.</li>
                    </ul>

                </p>
