Universidade Federal do Rio de Janeiro (UFRJ) - Brasil 
	
<p>

<p align="justify">


Possui graduação em Engenharia Elétrica pela Universidade Federal do Paraná (1971), mestrado em Engenharia Elétrica pela COPPE / Universidade Federal do Rio de Janeiro (1973), doutorado em Engenharia Elétrica pela University of Manchester Institute of Science and Technology, Reino Unido (1981) e pós-doutorado pela University of California at Berkeley, USA (1993). Atualmente é professor titular da COPPE / Universidade Federal do Rio de Janeiro e Bolsista de Produtividade em Pesquisa do CNPq - Nível SR. De 2003 a 2004 atuou como Assessor do Presidente da Eletrobras. Em 2004 foi elevado ao grau de Fellow do Institute of Electrical and Electronics Engineers (IEEE). Tem experiência na área de Engenharia Elétrica, com ênfase em Sistemas Elétricos de Potência, atuando principalmente nos seguintes temas: Sistemas de Potência, Inteligência Computacional, Processamento Paralelo e Redes Elétricas Inteligentes (Smart Grid)

