Valter Júnior de Souza Leite possui graduação (1995) e mestrado (1998) em Engenharia Elétrica pela Universidade Federal de Minas Gerais, doutorado em Engenharia Elétrica (2005) pela Universidade Estadual de Campinas e doutorado em Automatique Et Informatique Industrielle (2005) pelo Institut National Des Sciences Appliqées de Toulouse, França. Atualmente é professor da carreira do Ens. Básico, Técnico e Tecnológico (D-5/03, equivalente ao nível Associado 4) do CEFET-MG. Foi coordenador do Curso de Engenharia Mecatrônica (2013-2015). Orientador no Programa de Pós-Graduação em Engenharia Elétrica da associação ampla enre CEFET-MG e UFSJ, nível mestrado. Tem experiência na área de Engenharia Elétrica, com ênfase em Controle de Processos Eletrônicos, Retroalimentação, atuando principalmente nos seguintes temas: estabilidade robusta, estabilização de sistemas com incertezas, lmi, sistemas com atrasos nos estados.



