<h4><b>	<a target="_blank" href="http://lattes.cnpq.br/5680858790581538">Prof. Eugênio de Bona Castelan Neto</a></b></h4>
Universidade Federal de Santa Catarina (UFSC) - Brasil<br>
Bolsista de Produtividade em Pesquisa do CNPq - Nível 1D
<p>

<p align="justify">


Possui graduação em Engenharia Elétrica pela Universidade Federal de Santa Catarina - UFSC (1982), mestrado em Engenharia Elétrica pela UFSC (1985) e doutorado em Automática pelo LAAS du CNRS - Université de Toulouse III (Paul Sabatier) (1992). Realizou estágio pós-doutoral no LAAS, França, em 2003, como Pesquisador Visitante. É professor na Universidade Federal de Santa Catarina (UFSC) desde 1992, lotado a partir de 1997 no Departamento de Automação e Sistemas (DAS-UFSC) no qual é Professor Titular. Tem experiência na área de Controle e Automação, atuando principalmente nos seguintes temas: controle sob restrições, sistemas descritores, controle de sistemas mecatrônicos e teoria de controle. Foi Chefe de Departamento entre 2000 e 2002, Coordenador do Programa de Pós-Graduação em Engenharia de Automação e Sistemas (PPGEAS-UFSC), de dezembro de 2006 a junho de 2010, e Subcoordenador do PPGEAS-UFSC até junho de 2012.


