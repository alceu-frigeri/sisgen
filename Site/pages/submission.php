        <h2>Instruçőes para confecçăo dos artigos</h2>
        <hr>    
        
        <p align="justify">
            Os artigos poderăo ser submetidos em portuguęs, espanhol ou inglęs com até 8 páginas, em arquivo formato .pdf conforme o padrăo disponível (LateX ou Word). Os artigos aceitos para publicaçăo serăo limitados a 6 páginas. Páginas excedentes (máximo de 2 páginas) serăo aceitas mediante pagamento de taxa extra. 


        </p> 

