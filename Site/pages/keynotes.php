						<b > Short bio:</b>
						Professor of Real-time Computer Systems, at the School of Information Technology. Appointed in 2003. The majority of computers used today do not look like "everyday" computers in an office environment. Instead computers are included as embedded components in various products which we use, controlling telephone exchanges, car engines, electrical grids, aircraft etc. As a rule these embedded computers need to be able to exchange information in real-time ie the calculations being performed not only need to be correct but the point in time when the result is transmitted is also of prime importance.Magnus Jonsson's research is directed towards the architecture of embedded real-time systems, real-time communication, optical connective networks and connective networks for parallel and distributed real-time systems. These areas are assuming more importance as embedded real-time systems increasingly respond to rigorous performance demands and become commonplace. In other words higher demands are being put on communication networks and their real-time characteristics.
