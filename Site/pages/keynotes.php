					<b > Jay Lee </b> (University of Cincinnati - USA) 
					<p class="title">
						Industrial Big Data Analytics and Digital Twin Design for Future Industrial Systems
					</p>
				</p> 

				<div class="readmore">
					<p class="description">
						 In today’s competitive business environment, companies are facing challenges in dealing with big data issues for rapid decision making for improved productivity. Many manufacturing systems are not ready to manage big data due to the lack of smart analytics tools. Germany is leading a transformation toward 4th Generation Industrial Revolution (Industry 4.0) based on Cyber-Physical System based manufacturing and service innovation. As more software and embedded intelligence are integrated in industrial products and systems, predictive technologies can further intertwine intelligent algorithms with electronics and tether-free tools to manage big data to achieve transparency and productivity. First, industry transformation including Germany 4.0 and cyber-physical system will be introduced. Second, advanced prognostics technologies for smart product service systems with case studies will be presented. In addition, research advances in designing cloud-based cyber-physical modeling for next-generation products and service systems will be discussed.	
