    <h2>Porto Alegre</h2>
    <hr>  

    <p align="justify">
            Com cerca de  1,5 milhăo de habitantes, Porto Alegre é o centro econômico, cultural e político
	    do estado do Rio Grande do Sul, tendo a regiăo metropolitana (constituída por 31 cidades satélites) cerca de 4 milhőes de habitantes.
    </p>


<OL type="A">
<li>.	Dados Gerais:</li>
<UL>
<li>	Populaçăo: 1.500.000 habitantes;</li>
<li>	Clima na Primavera (Setembro a Dezembro): 20şC a 40şC;</li>
<li>	Facilidade de acesso:</li>
<OL type="i">
<li>	Em média 265 voos diários;</li>
<li>	A 1h30 das principais capitais brasileiras e do Mercosul;</li>
<li>	Conexăo direta com Săo Paulo, Rio e Brasília; </li>
<li>	Aeroporto apenas a 7Km (20 min) do centro da cidade, com ligaçăo via metrô e ônibus.</li>
</OL>
<li>	6Ş posiçăo no ranking de cidades brasileiras que mais recebem eventos internacionais (ICCA 2014);</li>
<li>	É referęncia internacional em qualidade de vida e Desenvolvimento Humano (IDH); </li>
<li>	Possui um sistema de mobilidade urbana privilegiado, com tempo médio de deslocamento entre aeroporto/hotéis/centros de eventos de 20 minutos.</li>
</UL>
<li>	Parque Industrial:</li>
Num raio de até 130Km de Porto Alegre estăo instaladas indústrias com alto grau de automaçăo como
<UL>

<li>	Tramontina - empresa mais automatizada do Brasil;</li>
<li>	Fábrica da General Motors;</li>
<li>	Polo Metal-Mecânico de Caxias do Sul: Marcopolo, Randon, entre outras;</li>
<li>	Refinaria Alberto Pasqualini e Polo Petroquímico;</li>
<li>	AEL Sistemas - indústria aeroespacial;</li>
<li>	Polo Eletroeletrônico e de Automaçăo: Altus, Novus, Elipse Software, Coester, Exatron, HT Micron, CEITEC-Semicondutores, entre outras;</li>
<li>	Polo vinícola e moveleiro de Bento Gonçalves - Aurora, Miolo, Todeschini;</li>
</UL>
No interior do estado é importante destacar o Polo Naval de Rio Grande e o Polo Agrícola, com empresas como AGCO, STARA e John Deere.

<li>	Estrutura Hoteleira:</li>
    <p align="justify">
Porto Alegre dispőe de um amplo parque hoteleiro com empreendimentos que variam da categoria Luxo ŕ Econômica. Săo mais de 100 meios de hospedagem que somam cerca de 18 mil leitos. A previsăo para os próximos anos é de um crescimento acentuado, devendo atingir 18 mil leitos até 2015.</p>
    <p align="justify">
O Centro da cidade e o bairro Moinhos de Vento concentram a maioria dos hotéis, permitindo acesso fácil e rápido para os grandes centros de eventos. A Regiăo Metropolitana proporciona outros 35 mil leitos em um raio de até 150km.</p>
<li>	Atraçőes Turísticas e Culturais:</li>
    <p align="justify">
Há muito para se ver e fazer em Porto Alegre, metrópole global e completa que năo perdeu de vista a qualidade de vida. E há coisas imperdíveis para quem quiser conhecer o melhor da capital gaúcha, como o histórico Mercado Público Central, movimentadíssimo espaço de compras e gastronomia, o Parque e Brique da Redençăo, a Usina do Gasômetro, a Fundaçăo Iberę Camargo e os 72 km da orla do Lago Guaíba e seu consagrado pôr do sol acompanhado de um bom chimarrăo. Entre as cidades mais arborizadas do país e segunda capital com a maior área rural preservada, Porto Alegre também é ideal para o turismo rural e para quem curte a vida ao ar livre.</p>
    <p align="justify">
A culinária gaúcha reúne a gastronomia étnica e típica. Uma opçăo tradicional é o Peixe na Taquara, preparado e servido por pescadores da Ilha da Pintada. Outro destaque săo as churrascarias que apresentam shows com músicas nativistas e danças folclóricas, como a Vitrine Gaúcha, Zequinha e Galpăo Crioulo. Os centros de Tradiçőes Gaúchas (CTGs) recebem com prazer aqueles que desejam conhecer a cultura gaúcha.</p>
    <p align="justify">
É ŕ noite que Porto Alegre revela uma das faces da sua multiculturalidade. A vida noturna é intensa e repleta de boas opçőes em eventos culturais, bares, choperias, pubs, cafés, casas de shows e teatros que movimentam diferentes bairros, cada um com seu charme, ritmo e estilo próprios. O programa pode ser um happy hour, dançar, paquerar, ouvir música ao vivo ou tomar um chopinho. Há lugares imperdíveis para se conhecer o melhor da cidade.</p>
</OL>




