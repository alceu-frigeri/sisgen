        <h2>Welcome to Symposium on Telematics Application 2016 </h2>
        <hr>
        <h4>Aim</h4>
        <p align="justify">
            The aim of the conference is to report on recent research and application of telematics. Researchers and practitioners are invited to discuss on recent developments, deployments, technology trends and research results, as well as initiatives related to telematics applications. Both, colleagues from academia and from industry are cordially invited to contribute to this tri-annual symposium.
        </p>

        <h4>Background</h4>
        <p align="justify">
            Telematics applications benefit from the tremendously increasing capabilities in Web technology, telecommunication, machine-to-machine communication,  and information processing. The concepts of Cyber Physical Systems, Industrial Internet and of Internet of Thinks enlarge the scope of classical remote control and offer several new applications. However, beyond sound reference examples of new technology one needs appropriate theory, methods, proof of concepts and industrial standardization as well. All these aspects are within the scope of the 4th IFAC Symposium on Telematics Applications.
        </p> 
        
