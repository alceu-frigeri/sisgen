<li>	Plenária: Prof. Jose C. Principe - A Cognitive Architecture for Object Recognition;
<ul>
<li>This talk describes our efforts to abstract from the animal visual system the computational principles to explain images in video. We develop a hierarchical, distributed architecture of dynamical systems that self-organizes to explain the input imagery using an empirical Bayes criterion with sparseness constraints and dual state estimation. The interpretation of the images is mediated through causes that flow top down and change the priors for the bottom up processing. We will present preliminary results in several data sets.
</ul>
<li>	Plenária: Prof. Prof. Shimon Nof - "Cyber-augmented collaboration of distributed e-Work and robotics";
<ul>
<li>Augmenting collaboration of distributed e-Work and robotics by effective cyber methods is becoming increasingly feasible, and widely desired to overcome obstacles in manufacturing, transportation, healthcare, agriculture, security, and more. Research and implementation of such cyber-augmented collaboration requires also the understanding of human and organizational augmentation needs for productive and smooth collaborative performance, on the way to achieving the intended goals. At the heart of cyber-augmentation is the Collaborative Control Theory (CCT). CCT and its design principles have been developed and validated for this purpose, and are described and illustrated in this presentation with key performance measures of emerging intelligent automation applications, collaborative robotics, and cyber-physical systems. Recently developed protocols of best matching, resilience by teaming, and dynamic lines of collaboration to overcome disruptions are also reviewed. Then, challenges and open questions will be discussed on how we can extend the current limits that have been observed. 
</ul>
<li>	Plenária: Prof. Frank Allgöwer - "The role of control for the smart manufacturing of the future";
<ul>
<li>With the vision of the smart factory of the future, generally termed Industry 4.0, the manufacturing industries are currently undergoing a fundamental new orientation on the basis of the Cyber-Physical Systems and Internet of Things and Services paradigms. All parts along the manufacturing chain are nowadays equipped with embedded computing, communication and networking capabilities and are expected to interact in an optimal way towards the goal of an energy and resource efficient, save and reliable production process. Through decentralized optimal decision-making and an appropriate communication among the networked individual parts, the whole production process of the future is expected to operate optimally. In this presentation the challenges and opportunities of Industry 4.0 for the field of automatic control are discussed. We will in particular investigate the potential impact of networked and optimization-based control for the fourth industrial revolution and will argue that some new developments in the control field, especially connected to distributed and economic model predictive control, appear to be ideally suited to have a potential  impact in the new Industry 4.0 environment.
</ul>
<li>	Plenária: Prof. Harald Mathis - "Health and Sensors: Digital transformation";
<ul>
<li>

We have to face a demographic change in many countries: People are getting older and older and suffer from a whole variety of age-related diseases. The rates of hospitalisation increase.Clinical and home surveillance  has to  include age-specific morbidity and mortality.
Now we have to answer the question, if it is possible to keep the aged people as long as possible at home to reduce costs but not the quality of services in medicine , care and general support.<br>

Here interdisciplinary engineering and the digital transformation can help and represent  excellent opportunities to solve the above mentioned problems. New ecosystems can be formed and new disruptive models are necessary. Sensors, electronics, software merge in combination with business models.
The presentation refers to digitally transformed processes and shows different applications on the field of body area networks and vital parameter measurement in the context of telemedical and clinical applications.
Furthermore the demographic change and the increased demand for medical care associated hereto as well as increasing acute and chronic diseases will ensure a stable growth in demand. the same applies for other market determinants, for example the increasing use of self medication, which is increasingly replacing out-patient and hospital care, as well as the rising number of generic and biotechnological products. The demographic change became an issue for German local authorities, since it is evident, that more and more municipalities have no medical service any more.<br>
Here new models of automation based on point-of-care systems in combination with IOT-based services can help to face this medical and care service problem and moreover can even reach a better quality of medical care than ever before.
THe talk will also refer to this situation and how it is possible to increase quality of different forms of care by sensor and IT-systems.


</ul>
<li>	Plenária: Prof. Marco Henrique Terra - "Controle Robusto Markoviano Recursivo Aplicado Em Robótica Cooperativa";
<ul>
<li>Nesta palestra trataremos de controle e filtragem de sistemas lineares sujeitos a incertezas paramétricas com aplicaçőes em robótica cooperativa. Os controles e filtros a serem apresentados levam em consideraçăo tanto sistemas incertos convencionais quanto aqueles que estăo sujeitos a saltos Markovianos. A estratégia desenvolvida para resolver tais problemas é baseada em mínimos quadrados regularizados robustos e funçőes penalidades. As soluçőes apresentadas săo baseadas em equaçőes algébricas de Riccati, tanto individuais quanto acopladas, que săo resolvidas recursivamente e năo dependem para isto de quaisquer parâmetros auxiliares. Essa abordagem é útil, portanto, para aplicaçőes em tempo real. Para exemplificar a eficięncia dessas estratégias dois tipos de aplicaçăo serăo apresentados. No primeiro, resultados de um sistema de controle tolerante a falhas para garantir o movimento coordenado de robôs móveis com rodas serăo apresentados. Uma formaçăo líder-seguidor é usada com uma topologia de comunicaçăo direcionada. A perda de comunicaçăo entre os robôs é identificada. Havendo restabelecimento da comunicaçăo entre os robôs o controle projetado restabelece as respectivas posiçőes dos robôs na formaçăo original. No segundo, apresentaremos um modelo unificado homem-robô para uso em reabilitaçăo de membros inferiores, onde săo identificados na dinâmica do robô modos fixos, resistivos e passivos usados na interaçăo com o paciente. Resultados de laboratório serăo apresentados em ambos os exemplos.
</ul>
<li>	Plenária: Prof. Jose Eduardo Ribeiro Cury - "Controle supervisório de sistemas biológicos celulares".
<ul>
<li>The ability to control the behavior of intracellular networks has relevant applications, ranging from an optimized synthesis of metabolites in bacteria to the cure of human diseases. To implement a controller in vivo, one may insert synthetic biomolecules into the cell being controlled. This seminar presents a formal approach, based on Supervisory Control Theory (SCT), to guide the design of synthetic genes that control the dynamics of gene regulatory networks, major players in cell dynamics, so that a set of specifications is achieved. Such a systematic procedure is important to make the control of large networks by synthetic molecules feasible. In this approach, it is assumed that the behavior of gene networks can be abstracted by finite state machines, where each state corresponds to a set of gene expression levels and the events are associated with the activation/repression of genes. 
</ul>
</ul>

