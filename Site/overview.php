<li>	Minicurso 04: Prof. Walter Fetter Lages - "Robot Operational System (ROS): Introduçăo e Implementaçăo de Controladores";
<ul>
<li>Este curso apresenta uma introduçăo ao Robot Operating System (ROS) com
enfoque na implementaçăo de controladores e sua interaçăo com robôs reais ou
simulados no Gazebo.  Os conceitos básicos do ROS săo abordados no contexto
de sua utilizaçăo na implementaçăo de controladores avançados para robôs.

Apesar do ROS ser um framework largamente utilizado atualmente, a sua
documentaçăo e os exemplos abordando controladores săo pobres a quase todas
as implementaçőes săo baseadas em controladores single input, single output
(SISO) utilizando a lei de controle Proporcional, Integral, Derivativa
(PID).  A maioria das referęncias e livros sobre o ROS sequer cobre a
implementaçăo de controladores.  Por outro lado, robôs săo em geral,
sistemas năo lineares multi input, multi output (MIMO), para os quais o uso
de controladores PID independentes para cada grou de liberdade (DoF) năo é
adequado se é desejado alto desempenho.

Este curso aborda de forma genérica a implementaçăo de controladores no ROS
e seu enfoque pode ser usado para a implementaçăo de qualquer lei de
controle (linear, năo linear, SISO ou MIMO) enquanto que os controladores
disponíveis nos pacotes padrăo do ROS como o ros_controllers săo apenas
representativos de controladores SISO.

O funcionamento interno do laço de tempo real do ROS é explicado em detalhe,
motivando a implementaçăo de controladores avançados no ROS.

Do ponto de vista de Engenharia de Controle, o ROS apresenta algumas
particularidades com relaçăo aos controladores.  Uma delas é relacionada ŕ
nomenclatura, já que a denominaçăo de controlador no ROS năo necessariamente
refere-se ŕ um controlador na nomenclatura de Sistemas de Controle.  Um
controlador no ROS é um plugin para o gerenciador de controladores que
implementa a interface Controller e tipicamente atua a junta através de
algum objeto que exponha uma JointCommandInterface e obtém dados de sensores
através de um objeto que exponha uma JointStateInterface.  Note que um
controlador ROS pode realizar uma funçăo que năo necessariamente é a funçăo
de um controlador em us sistema de controle.  Um exemplo clássico é o
JointStateController, que por seu nome parece ser um controlador de juntas
no espaço de estados, mas que na realidade é apenas um publicador dos
valores das posiçőes e velocidades das juntas.

O ROS năo é um sistema de tempo real.  No entanto, ele possui alguma
capacidade de tempo real quando executado em um sistema Linux que suporte
tempo real através dos patches PREEMP_RT, RTAI ou XENOMAI e a efetiva
implementaçăo de controladores em tempo real utilizando tais patches é
discutida.

Como os controladores no ROS săo plugins carregados pelo gerenciador de
controladores, o seu uso para carta, descarga, ativaçăo e desativaçăo de
controladores é abordado juntamente com os principais pacotes do ROS para
implementaçăo de controladores, mais especificamente: control_toolbox,
controller_interface, controller_manager, hardware_interface,
realtime_tools, effort_controllers, joint_state_controller,
ros_control_gazebo, ros_control_gazebo_plugin, robot_state_publisher, entre
outros.

Săo apresentados em detalhes a implementaçăo de um controlador por torque
calculado para um manipulador robótico, um controlador linearizante e um
controlador năo linear para um robô móvel, além do uso do controladores de
bypass e PID disponíveis nos pacotes ROS.

Palavras-chave:

ROS, Introduçăo, Controlador, Implementaçăo Tempo real, Controlador năo linear.

Syllabus:

Introduçăo ao ROS, Descriçăo de robôs no ROS, Unified Robot Description
Format (URDF), Visualizaçăo no rviz, Simulaçăo no Gazebo, Mensagens,
serviços e aőes, Controlador bypass, Controlador PID, Gerenciador de
controladores, Laço de tempo real do ROS, Implementaçăo de controladores,
Suporte de tempo real, Interaçăo com o simulador Gazebo, Interaçăo com robô
real.

</ul>
