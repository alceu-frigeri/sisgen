<li>	Minicurso 02: Profs. Eugęnio de Bona Castelan Neto e Valter Júnior de Souza Leite - "Modelagem e controle fuzzy-TS de  sistemas năo-lineares e com atraso nos estados";
<ul>
    <li>Neste mini-curso, busca-se evidenciar a importância da consideraçăo da validade regional dos modelos fuzzy de tipo Takagi-Sugeno (T-S) para o desenvolvimento de ferramentas de análise e síntese de sistemas de controle năo lineares,  e discutir problemas associados ŕ complexidade desses modelos dos pontos de vista de implementaçăo numérica dos  algoritmos e de implementaçăo computacional dos controladores. Serăo considerados sistemas năo lineares discretos no tempo, inicialmente sem a presença de atrasos e, a seguir, sistemas com atrasos nos estados.

Modelos fuzzy T-S tęm sido investigados no decorrer das últimas décadas principalmente por propiciarem o desenvolvimento de metodologias que possuem caráter sistemático para análise e projeto de sistemas de controle năo lineares, além de propiciar soluçőes numéricas via técnicas baseadas em desigualdades matriciais lineares (LMIs, do inglęs linear matrix inequalities). Embora diversos resultados sejam encontrados na literatura, existem questőes de motivaçăo prática que devem também ser consideradas no contexto do controle fuzzy baseado em modelo (FMB, do inglęs Fuzzy Model Based). Primeiramente, as técnicas de modelagem fuzzy T-S  garantem a convexidade do modelo e/ou a sua precisăo de representaçăo somente para uma determinada regiăo do espaço de estados. Portanto, para estratégias de controle que dependem da propriedades de convexidade, a estabilidade do sistema de malha fechada formado pelo sistema năo linear realimentado pela lei de controle fuzzy deve ser estudada no contexto de estabilidade local. Assim, é fundamental a determinaçăo de regiőes de estabilidade para o sistema de malha fechada. Essa importante característica dos modelos fuzzy T-S raramente é considerada na literatura, podendo implicar em perda de desempenho e até mesmo instabilidade do sistema em malha fechada. Outro problema inerente ŕ utilizaçăo de modelos fuzzy T-S diz respeito ao aumento exponencial de complexidade do modelo com o número de năo linearidades presentes no sistema, principalmente quando se busca descrever de forma exata a dinâmica do sistema a controlar. Isso implica no aumento da complexidade numérica dos algoritmos para análise e projeto, assim como no aumento da complexidade de implementaçăo de leis de controle.

</ul>
